package Maths;

public class Operation {
	public void add() {
		int a,b,c;
		a=10;
		b=15;
		c=a+b;
		System.out.println(c);
	}
	public void sub() {
		int d,e,f;
		d=10;
		e=15;
		f=d-e;
		System.out.println(f);
	}
	public void mult() {
		int l,m,n;
		l=10;
		m=15;
		n=l*m;
		System.out.println(n);
	}
	public void div() {
		int x,y,z;
		x=10;
		y=5;
		z=x/y;
		System.out.println(z);
	}
}
